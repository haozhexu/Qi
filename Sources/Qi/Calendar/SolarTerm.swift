import Foundation

/// 二十四節
public enum SolarTerm: String, CaseIterable, Equatable {

    /// 立春
    /// when spring begins
    case springBegins

    /// 雨水
    /// rain starts and increasing
    case theRains

    /// 驚蟄
    /// Spring thunder comes in a sudden and awakens the hibernating animals in the soil
    case insectsAwaken

    /// 春分
    /// the moment when day and night have same length
    case vernalEquinox

    /// 清明
    /// sunny, lush vegetation
    case clearAndBright

    /// 谷雨
    /// sufficient and timely rainfall, crops thrive
    case grainRain

    /// 立夏
    /// summer begins
    case summerBegins

    /// 小滿
    /// grains of summer crops such as wheat begin to fill up
    case grainBuds

    /// 芒種
    /// wheat and other awn crops mature
    case grainInEar

    /// 夏至
    /// hot summer comes, the sun reached the Northern Tropic
    case summerSolstice

    /// 小暑
    /// whether starts to be hot
    case slightHeat

    /// 大暑
    /// hottest moment in year
    case greatHeat

    /// 立秋
    /// autumn beings
    case autumnBegins

    /// 處暑
    /// the hot summer is over
    case heatStops

    /// 白露
    /// weather turns cool, the dew becomes white
    case whiteDews

    /// 秋分
    /// the moment when day and night have same length
    case autumnEquinox

    /// 寒露
    /// the dew is cold and about to freeze
    case coldDews

    /// 霜降
    /// the weather is getting cold, frost starts
    case frostFalls

    /// 立冬
    /// winter begins
    case winterBegins

    /// 小雪
    /// light snow comes
    case lightSnow

    /// 大雪
    /// heavy snow
    case heavySnow

    /// 冬至
    /// winter begins
    case winterSolstice

    /// 小寒
    /// the weather starts to become cold
    case slightCold

    /// 大寒
    /// the coldest moment in a year
    case greatCold

    public struct MonthAndDay: Equatable, Comparable {
        public let month: Int
        public let day: Int

        public static func < (lhs: SolarTerm.MonthAndDay, rhs: SolarTerm.MonthAndDay) -> Bool {
            if lhs.month < rhs.month {
                return true
            } else if lhs.month == rhs.month {
                return lhs.day < rhs.day
            }
            return false
        }
    }

    /// 日期對應節氣
    public static func of(_ date: Date) -> SolarTerm? {
        guard let info = dateInfo(for: date) else {
            return nil
        }
        if info.day >= 15 {
            return info.solarTermsInTheMonth.1.solarTerm
        } else {
            return info.solarTermsInTheMonth.0.solarTerm
        }
    }

    /// 指定節氣在指定年的月日
    public static func monthAndDay(of solarTerm: SolarTerm, in year: Int) -> MonthAndDay {
        guard year >= 1901 && year <= 2100 else {
            return solarTerm.roughMonthAndDay
        }
        guard let solarTermIndex = Constants.hkoSolarTerms.firstIndex(of: solarTerm) else {
            preconditionFailure("Cannot get index of \(solarTerm) in \(Constants.hkoSolarTerms)!")
        }
        let monthIndex = solarTermIndex / 2
        let month = monthIndex + 1
        let offset = solarTermIndex % 2
        let index = (year - 1901) * 12 + monthIndex
        let flag = Constants.hkoSolarTerms1901To2100[index]
        if offset == 0 {
            let day = (flag >> 4) & 0x0F
            return .init(month: month, day: day)
        } else {
            let day = (flag & 0x0F) + 15
            return .init(month: month, day: day)
        }
    }

    private struct DateInfo {
        let date: Date
        let year: Int
        let month: Int
        let day: Int
        let lookupIndex: Int
        let solarTermsInTheMonth: ((solarTerm: SolarTerm, day: Int), (solarTerm: SolarTerm, day: Int))
    }

