    private static func dateInfo(for date: Date) -> DateInfo? {
        let comps = Calendar(identifier: .gregorian)
            .dateComponents([.year, .month, .day], from: date)
        guard let year = comps.year,
              let month = comps.month,
              let day = comps.day else {
            return nil
        }
        guard year >= 1901 && year <= 2100 else {
            return nil
        }
        let lookupIndex = (year - 1901) * 12 + (month - 1)
        let (firstDay, secondDay) = solarTermDays(index: lookupIndex)
        let firstSolarIndex = (month - 1) * 2
        let secondSolarIndex = (month - 1) * 2 + 1
        let firstSolarTerm = Constants.hkoSolarTerms[firstSolarIndex]
        let secondSolarTerm = Constants.hkoSolarTerms[secondSolarIndex]

        return DateInfo(date: date,
                 year: year,
                 month: month,
                 day: day,
                 lookupIndex: lookupIndex,
                 solarTermsInTheMonth: ((firstSolarTerm, firstDay), (secondSolarTerm, secondDay)))
    }

    private static func solarTermDays(index: Int) -> (Int, Int) {
        let days = Constants.hkoSolarTerms1901To2100[index]
        let first = (days >> 4) & 0x0F
        let second = (days & 0x0F) + 15
        return (first, second)
    }

